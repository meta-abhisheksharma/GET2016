package Tic_Tac_Toe;

public class Board {
	static final int Empty=0;
	static final int ROW=3,COL=3;
	int board[][]= new int[ROW][COL];
	
	Board()
	{
		for(int row=0; row < ROW; row++)
		{
			for(int col=0;col<COL;col++)
			{
				board[row][col]=Empty;
			}
		}
		
	}
	
	public void setBoard(int board[][]){
		this.board=board;
	}
	
	public int[][] getBoard(){
		return board;
	}

}
